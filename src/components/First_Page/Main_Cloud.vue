      tag.href = "https://baidu.com/"; //给标签添加链接
      tag.choice = false; //标记鼠标是否悬停选中
      tagsTemp.push(tag);
    }
    this.tags = tagsTemp; //让vue替我们完成视图更新
  },
  methods: {
    //下面就是通过改变每一个tag的x,y的值来使球旋转起来；实现方法是rotateX,rotateY函数：
    rotateX(angleX) {
      var cos = Math.cos(angleX);
      var sin = Math.sin(angleX);
      for (let tag of this.tags) {
        var y1 = (tag.y - this.CY) * cos - tag.z * sin + this.CY;
        var z1 = tag.z * cos + (tag.y - this.CY) * sin;
        tag.y = y1;
        tag.z = z1;
      }
    },
    rotateY(angleY) {
      var cos = Math.cos(angleY);
      var sin = Math.sin(angleY);
      for (let tag of this.tags) {
        var x1 = (tag.x - this.CX) * cos - tag.z * sin + this.CX;
        var z1 = tag.z * cos + (tag.x - this.CX) * sin;
        tag.x = x1;
        tag.z = z1;
      }
    },
    setOpacity(tag) {
      if (!this.allowMove && !tag.choice) {
        //停止动画且未被选中，调整不透明度
        return 0.17;
      }
      if (!this.allowMove && tag.choice) {
        //停止动画且选中，不透明度为1
        return 1;
      }
      return (400 + tag.z) / 600;
    },
    setfontSize(tag) {
      if (tag.choice) {
        //鼠标悬停处字体放大
        return 30 * (600 / (600 - tag.z));
      }
      return 25 * (600 / (600 - tag.z));
    },
    hoverListener(event, tag) {
      //响应鼠标悬停
      this.allowMove = false;
      tag.choice = true;
    },
    leaveListener(event, tag) {
      //响应鼠标离开
      this.allowMove = true;
      tag.choice = false;
    },
