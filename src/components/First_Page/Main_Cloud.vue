function randomDiffNum(range, n) {
  //生成n个 [0, range)随机数 返回num数组
  var num = [];
  for (var i = 0; i < n; i++) {
    num[i] = Math.floor(Math.random() * range);
    for (var j = 0; j < i; j++) {
      if (num[i] == num[j]) {
        i--;
      }
    }
  }
  return num;
}
